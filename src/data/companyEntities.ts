export function getCompanyEntity(idOrSlug: string): CompanyEntity | undefined {
  return companyEntities.find(c => c.id === idOrSlug || c.slug === idOrSlug);
}

export function getCompaniesByProduct(productSlug: string): CompanyEntity[] {
  return companyEntities.filter(c => c.products.includes(productSlug));
}

export function getCompaniesByCompetitor(companyId: string): CompanyEntity[] {
  const company = getCompanyEntity(companyId);
  if (!company) return [];
  return companyEntities.filter(c => company.competitors.includes(c.id));
}

export function getCompaniesByType(): Record<string, CompanyEntity[]> {
  const types: Record<string, CompanyEntity[]> = {};
  companyEntities.forEach(c => {
    const key = c.aiFocus[0] || 'Other';
    if (!types[key]) types[key] = [];
    types[key].push(c);
  });
  return types;
}
