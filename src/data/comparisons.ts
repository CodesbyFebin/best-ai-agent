export function getRelatedComparisons(slug: string): ComparisonPage[] {
  const page = comparisonPages.find(c => c.slug === slug);
  if (!page) return [];
  return comparisonPages.filter(c => c.slug !== slug && c.relatedSlugs.some(s => page.relatedSlugs.includes(s))).slice(0, 3);
}
