export function getMcpEntity(idOrSlug: string): McpEntity | undefined {
  return mcpEntities.find(m => m.id === idOrSlug || m.slug === idOrSlug);
}

export function getMcpByCategory(category: McpEntity['category']): McpEntity[] {
  return mcpEntities.filter(m => m.category === category);
}

export function getMcpByProtocol(protocol: McpEntity['protocol']): McpEntity[] {
  return mcpEntities.filter(m => m.protocol === protocol);
}

export function getMcpByTransport(transport: McpEntity['transport']): McpEntity[] {
  return mcpEntities.filter(m => m.transport === transport);
}

export function getSelfHostedMcp(): McpEntity[] {
  return mcpEntities.filter(m => m.selfHosted);
}

export function getCloudHostedMcp(): McpEntity[] {
  return mcpEntities.filter(m => m.cloudHosted);
}

export function getOpenSourceMcp(): McpEntity[] {
  return mcpEntities.filter(m => m.openSource);
}

export function getMcpByMaturity(maturity: McpEntity['maturity']): McpEntity[] {
  return mcpEntities.filter(m => m.maturity === maturity);
}

export function getMcpBySecurityRating(rating: McpEntity['securityRating']): McpEntity[] {
  return mcpEntities.filter(m => m.securityRating === rating);
}

export function getTopMcpServers(limit = 10): McpEntity[] {
  return [...mcpEntities].sort((a, b) => b.popularity - a.popularity).slice(0, limit);
}

export function getMcpByAgent(agentId: string): McpEntity[] {
  return mcpEntities.filter(m => m.agents.includes(agentId));
}

export function getMcpByFramework(frameworkId: string): McpEntity[] {
  return mcpEntities.filter(m => m.frameworks.includes(frameworkId));
}

export function getMcpToolCount(entity: McpEntity): number {
  return entity.tools.length;
}

export function getMcpTotalTools(): number {
  return mcpEntities.reduce((sum, m) => sum + m.tools.length, 0);
}
